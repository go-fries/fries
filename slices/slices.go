func GroupByN[S ~[]E, E any, K comparable](s S, fn func(E, int) K) map[K]S {
	result := make(map[K]S)
	for i, item := range s {
		key := fn(item, i)
		result[key] = append(result[key], item)
	}
	return result
}

// CountBy returns a new map containing item counts grouped by the key returned
// by fn.
//
//	CountBy([]int{1, 2, 3, 4}, func(i int) string {
//		if i%2 == 0 {
//			return "even"
//		}
//		return "odd"
//	}) // map[string]int{"even": 2, "odd": 2}
func CountBy[S ~[]E, E any, K comparable](s S, fn func(E) K) map[K]int {
	result := make(map[K]int)
	for _, item := range s {
		key := fn(item)
		result[key]++
	}
	return result
}

// First returns the first item in s, or a zero value and false if s is empty.
//
//	First([]int{1, 2, 3}) // 1, true
