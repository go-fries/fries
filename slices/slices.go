// Difference returns a new slice containing items in s1 that are not present in
// s2.
//
//	Difference([]int{1, 2, 3}, []int{2, 3, 4}) // []int{1}
//	Difference([]string{"a", "b", "c"}, []string{"b", "c", "d"}) // []string{"a"}
func Difference[S ~[]E, E comparable](s1, s2 S) S {
	var result S
	for _, item := range s1 {
		if !slices.Contains(s2, item) {
			result = append(result, item)
		}
	}
	return result
}

// Intersect returns a new slice containing items in s1 that are also present in
// s2.
//
//	Intersect([]int{1, 2, 3}, []int{2, 3, 4}) // []int{2, 3}
//	Intersect([]string{"a", "b", "c"}, []string{"b", "c", "d"}) // []string{"b", "c"}
func Intersect[S ~[]E, E comparable](s1, s2 S) S {
	var result S
	for _, item := range s1 {
		if slices.Contains(s2, item) {
			result = append(result, item)
		}
	}
	return result
}

// Only returns a new slice containing only the selected items from s.
//
//	Only([]int{1, 2, 3}, 2, 3) // []int{2, 3}
//	Only([]string{"a", "b", "c"}, "b", "c") // []string{"b", "c"}
func Only[S ~[]E, E comparable](s S, items ...E) S {
	var result S
	for _, item := range s {
		if slices.Contains(items, item) {
			result = append(result, item)
		}
	}
	return result
}

// Without returns a new slice without the selected items from s.
//
//	Without([]int{1, 2, 3}, 2, 3) // []int{1}
//	Without([]string{"a", "b", "c"}, "b", "c") // []string{"a"}
func Without[S ~[]E, E comparable](s S, items ...E) S {
	var result S
	for _, item := range s {
		if !slices.Contains(items, item) {
			result = append(result, item)
		}
	}
	return result
}

// Remove returns a new slice without the selected items from s.
// Remove is an alias for [Without].
//
//	Remove([]int{1, 2, 3}, 2, 3) // []int{1}
//	Remove([]string{"a", "b", "c"}, "b", "c") // []string{"a"}
func Remove[S ~[]E, E comparable](s S, items ...E) S {
	return Without(s, items...)
}

// Partition returns two new slices. The first contains items in s that satisfy
// fn, and the second contains items that do not satisfy fn.
//
//	Partition([]int{1, 2, 3}, func(i int) bool { return i > 1 }) // ([]int{2, 3}, []int{1})
func Partition[S ~[]E, E any](s S, fn func(E) bool) (yes, no S) {
	for _, item := range s {
		if fn(item) {
			yes = append(yes, item)
		} else {
			no = append(no, item)
		}
	}
	return yes, no
}

// PartitionN returns two new slices. The first contains items in s that satisfy
// fn, and the second contains items that do not satisfy fn. The current index is
// passed to fn as the second argument.
//
//	PartitionN([]int{1, 2, 3}, func(i, idx int) bool { return i > 1 }) // ([]int{2, 3}, []int{1})
func PartitionN[S ~[]E, E any](s S, fn func(E, int) bool) (yes, no S) {
	for i, item := range s {
		if fn(item, i) {
			yes = append(yes, item)
		} else {
			no = append(no, item)
		}
	}
	return yes, no
}

// Chunk returns a new slice containing the items in s chunked into smaller
// slices of size.
//
// If size is less than or equal to zero, Chunk returns nil.
//
//	Chunk([]int{1, 2, 3, 4, 5}, 2) // [][]int{{1, 2}, {3, 4}, {5}}
//	Chunk([]string{"a", "b", "c", "d", "e"}, 2) // [][]string{{"a", "b"}, {"c", "d"}, {"e"}}
func Chunk[S ~[]E, E any](s S, size int) (result []S) {
	if size <= 0 {
		return nil
	}

	length := len(s)
	for i := 0; i < length; i += size {
		end := min(i+size, length)
		result = append(result, s[i:end])
	}
	return result
}

// GroupBy returns a new map containing the items in s grouped by the key
// returned by fn.
//
//	GroupBy([]int{1, 2, 3, 4, 5}, func(i int) int { return i % 2 }) // map[int][]int{0: {2, 4}, 1: {1, 3, 5}}
func GroupBy[S ~[]E, E any, K comparable](s S, fn func(E) K) map[K]S {
	result := make(map[K]S)
	for _, item := range s {
		key := fn(item)
		result[key] = append(result[key], item)
	}
	return result
}

// GroupByN returns a new map containing the items in s grouped by the key
// returned by fn. The current index is passed to fn as the second argument.
