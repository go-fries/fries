// Map returns a new slice containing the results of applying fn to each item in s.
//
//	Map([]int{1, 2, 3}, func(i int) int { return i * 2 }) // []int{2, 4, 6}
//	Map([]string{"a", "b", "c"}, func(s string) string { return s + "!" }) // []string{"a!", "b!", "c!"}
func Map[S ~[]E, E, R any](s S, fn func(E) R) []R {
	result := make([]R, 0, len(s))
	for _, item := range s {
		result = append(result, fn(item))
	}
	return result
}

// MapN returns a new slice containing the results of applying fn to each item in s.
// The current index is passed to fn as the second argument.
//
//	Map([]int{1, 2, 3}, func(i, _ int) int { return i * 2 }) // []int{2, 4, 6}
//	Map([]string{"a", "b", "c"}, func(s string, _ int) string { return s + "!" }) // []string{"a!", "b!", "c!"}
func MapN[S ~[]E, E, R any](s S, fn func(E, int) R) []R {
	result := make([]R, 0, len(s))
	for i, item := range s {
		result = append(result, fn(item, i))
	}
	return result
}

// FlatMap returns a new slice containing the concatenated results of applying
// fn to each item in s.
//
//	FlatMap([]int{1, 2}, func(i int) []int { return []int{i, i * 10} }) // []int{1, 10, 2, 20}
func FlatMap[S ~[]E, E, R any](s S, fn func(E) []R) []R {
	result := make([]R, 0, len(s))
	for _, item := range s {
		result = append(result, fn(item)...)
	}
	return result
}

// KeyMap returns a new map containing keys and values generated by applying fn
// to each item in s.
//
//	KeyMap([]int{1, 2, 3}, func(i, _ int) (int, int) { return i, i * 2 }) // map[int]int{1: 2, 2: 4, 3: 6}
//	KeyMap([]string{"a", "b", "c"}, func(s string, _ int) (string, string) {
//		return s, s + "!"
//	}) // map[string]string{"a": "a!", "b": "b!", "c": "c!"}
func KeyMap[S ~[]E, E, R any, K comparable](s S, fn func(E, int) (K, R)) map[K]R {
	result := make(map[K]R)
	for i, item := range s {
		key, value := fn(item, i)
		result[key] = value
	}
	return result
}

// Each calls fn for each item in s.
//
//	Each([]int{1, 2, 3}, func(i int) { fmt.Println(i) }) // 1\n2\n3
func Each[S ~[]E, E any](s S, fn func(E)) {
	for _, item := range s {
		fn(item)
	}
}

// EachN calls fn for each item in s.
// The current index is passed to fn as the second argument.
//
//	EachN([]int{1, 2, 3}, func(i int, _ int) { fmt.Println(i) }) // 1\n2\n3
func EachN[S ~[]E, E any](s S, fn func(E, int)) {
	for i, item := range s {
		fn(item, i)
	}
}

// Prepend returns s with items added to the beginning.
//
//	Prepend([]int{1, 2, 3}, 4, 5) // []int{4, 5, 1, 2, 3}
func Prepend[S ~[]E, E any](s S, items ...E) S {
	return append(items, s...)
}

// Append returns s with items added to the end.
//
//	Append([]int{1, 2, 3}, 4, 5) // []int{1, 2, 3, 4, 5}
func Append[S ~[]E, E any](s S, items ...E) S {
	return append(s, items...)
}

// Filter returns a new slice containing the items in s that satisfy fn.
//
//	Filter([]int{1, 2, 3}, func(i int) bool { return i > 1 }) // []int{2, 3}
//	Filter([]string{"a", "b", "c"}, func(s string) bool { return s != "b" }) // []string{"a", "c"}
func Filter[S ~[]E, E any](s S, fn func(E) bool) S {
	var result S
	for _, item := range s {
		if fn(item) {
			result = append(result, item)
		}
	}
	return result
}

// FilterN returns a new slice containing the items in s that satisfy fn.
// The current index is passed to fn as the second argument.
//
//	FilterN([]int{1, 2, 3}, func(i int, _ int) bool { return i > 1 }) // []int{2, 3}
//	FilterN([]string{"a", "b", "c"}, func(s string, _ int) bool { return s != "b" }) // []string{"a", "c"}
func FilterN[S ~[]E, E any](s S, fn func(E, int) bool) S {
	var result S
	for i, item := range s {
		if fn(item, i) {
			result = append(result, item)
		}
	}
	return result
}

// Any reports whether any item in s satisfies fn.
//
//	Any([]int{1, 2, 3}, func(i int) bool { return i > 2 }) // true
func Any[S ~[]E, E any](s S, fn func(E) bool) bool {
	return slices.ContainsFunc(s, fn)
}

// Every reports whether every item in s satisfies fn.
//
// Every returns true for an empty slice.
//
//	Every([]int{1, 2, 3}, func(i int) bool { return i > 0 }) // true
func Every[S ~[]E, E any](s S, fn func(E) bool) bool {
	for _, item := range s {
		if !fn(item) {
			return false
		}
	}
	return true
}

// Reduce applies fn against an accumulator and each item in s to reduce it to a
// single value.
//
//	Reduce([]int{1, 2, 3}, func(acc, i int) int { return acc + i }, 0) // 6
//	Reduce([]string{"a", "b", "c"}, func(acc, s string) string { return acc + s }, "") // "abc"
//	Reduce([]int{1, 2, 3}, func(acc, i int) int { return acc + i }, 5) // 11
func Reduce[S ~[]E, E, R any](s S, fn func(R, E) R, defaults ...R) R {
	var result R
	if len(defaults) > 0 {
		result = defaults[0]
	}
	for _, item := range s {
		result = fn(result, item)
	}
	return result
}

// ReduceN applies fn against an accumulator and each item in s to reduce it to a
// single value. The current index is passed to fn as the third argument.
//
//	ReduceN([]int{1, 2, 3}, func(acc, i, _ int) int { return acc + i }, 0) // 6
//	ReduceN([]string{"a", "b", "c"}, func(acc, s string, _ int) string { return acc + s }, "") // "abc"
//	ReduceN([]int{1, 2, 3}, func(acc, i, _ int) int { return acc + i }, 5) // 11
func ReduceN[S ~[]E, E, R any](s S, fn func(R, E, int) R, defaults ...R) R {
	var result R
	if len(defaults) > 0 {
		result = defaults[0]
	}
	for i, item := range s {
		result = fn(result, item, i)
	}
	return result
}

// Reverse returns a new slice containing the items in s in reverse order.
//
//	Reverse([]int{1, 2, 3}) // []int{3, 2, 1}
func Reverse[S ~[]E, E any](s S) S {
	result := make(S, len(s))
	for i := 0; i < len(s); i++ {
		result[i] = s[len(s)-1-i]
	}
	return result
}

// Concat returns a new slice containing the items of all given slices.
//
//	Concat([]int{1, 2}, []int{3, 4}, []int{5, 6}) // []int{1, 2, 3, 4, 5, 6}
func Concat[S ~[]E, E any](slices ...S) S {
	var totalLen int
	for _, slice := range slices {
		totalLen += len(slice)
	}

	result := make(S, 0, totalLen)
	for _, slice := range slices {
		result = append(result, slice...)
	}
	return result
}

// IsEmpty reports whether s is empty.
//
//	IsEmpty([]int{}) // true
//	IsEmpty([]int{1, 2, 3}) // false
func IsEmpty[S ~[]E, E any](s S) bool {
	return len(s) == 0
}

// IsNotEmpty reports whether s is not empty.
//
//	IsNotEmpty([]int{}) // false
//	IsNotEmpty([]int{1, 2, 3}) // true
func IsNotEmpty[S ~[]E, E any](s S) bool {
	return len(s) > 0
}

// IndexOf returns the index of the first occurrence of e in s, or -1 if e is
// not present.
//
//	IndexOf([]int{1, 2, 3}, 2) // 1
//	IndexOf([]string{"a", "b", "c"}, "d") // -1
func IndexOf[S ~[]E, E comparable](s S, e E) int {
	for i, item := range s {
		if item == e {
			return i
		}
	}
	return -1
}

// LastIndexOf returns the index of the last occurrence of e in s, or -1 if e is
// not present.
//
//	LastIndexOf([]int{1, 2, 3, 2}, 2) // 3
//	LastIndexOf([]string{"a", "b", "c"}, "d") // -1
func LastIndexOf[S ~[]E, E comparable](s S, e E) int {
	for i := len(s) - 1; i >= 0; i-- {
		if s[i] == e {
			return i
		}
	}
	return -1
}

// Unique returns a new slice containing the unique items in s.
//
//	Unique([]int{1, 2, 2, 3, 3, 3}) // []int{1, 2, 3}
//	Unique([]string{"a", "b", "b", "c", "c", "c"}) // []string{"a", "b", "c"}
func Unique[S ~[]E, E comparable](s S) S {
	var result S
	seeds := make(map[E]struct{})
	for _, item := range s {
		if _, ok := seeds[item]; !ok {
			seeds[item] = struct{}{}
			result = append(result, item)
		}
	}
	return result
}

