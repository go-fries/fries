// Random returns a random item from s.
//
// Random uses math/rand/v2 and returns the zero value if s is empty.
//
//	Random([]int{1, 2, 3}) // 2
//	Random([]string{"a", "b", "c"}) // "b"
func Random[S ~[]E, E any](s S) E {
	if len(s) == 0 {
		var zero E
		return zero
	}
	return s[rand.IntN(len(s))]
}

// Shuffle returns a new slice containing the items in s shuffled.
//
//	Shuffle([]int{1, 2, 3}) // []int{3, 1, 2}
func Shuffle[S ~[]E, E any](s S) S {
	result := make(S, len(s))
	copy(result, s)
	rand.Shuffle(len(result), func(i, j int) {
		result[i], result[j] = result[j], result[i]
	})
	return result
}

// Min returns the minimum item in s.
//
// If s is empty, Min returns the zero value.
//
//	Min([]int{1, 2, 3}) // 1
//	Min([]string{"a", "b", "c"}) // "a"
func Min[S ~[]E, E constraints.Ordered](s S) E {
	if len(s) == 0 {
		var zero E
		return zero
	}

	m := s[0]
	for _, item := range s {
		if item < m {
			m = item
		}
	}
	return m
}

// Max returns the maximum item in s.
//
// If s is empty, Max returns the zero value.
//
//	Max([]int{1, 2, 3}) // 3
//	Max([]string{"a", "b", "c"}) // "c"
func Max[S ~[]E, E constraints.Ordered](s S) E {
	if len(s) == 0 {
		var zero E
		return zero
	}

	m := s[0]
	for _, item := range s {
		if item > m {
			m = item
		}
	}
	return m
}

// Sum returns the sum of the items in s.
//
//	Sum([]int{1, 2, 3}) // 6
func Sum[S ~[]E, E constraints.Numeric](s S) E {
	var sum E
	for _, item := range s {
		sum += item
	}
	return sum
}

// Length returns the length of s.
//
//	Length([]int{1, 2, 3}) // 3
//	Length([]string{"a", "b", "c"}) // 3
func Length[S ~[]E, E any](s S) int {
	return len(s)
}
